// --- 辅助函数 ---
function getCardById(id) {
  return TAROT_CARDS.find(c => c.id === id);
}

function getMajorArcana() {
  return TAROT_CARDS.filter(c => c.arcana === 'major');
}

function getMinorArcana() {
  return TAROT_CARDS.filter(c => c.arcana === 'minor');
}

function getCardsBySuit(suit) {
  return TAROT_CARDS.filter(c => c.suit === suit);
}
